# add_numbers.py

def add_two_numbers(a, b):
    """
    This function takes two numbers as input and returns their sum.
    """
    return a + b

# Example usage:
num1 = 5
num2 = 10
total = add_two_numbers(num1, num2)
print(f"The sum of {num1} and {num2} is {total}")
# Output: The sum of 5 and 10 is 15
